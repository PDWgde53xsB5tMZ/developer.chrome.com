// Importing the 'ava' testing library to write unit tests for the Aside component.
const test = require('ava');

// Importing 'cheerio' to enable DOM manipulation and traversal for testing.
const cheerio = require('cheerio');

// Importing the Aside component from the specified file path.
const {Aside} = require('../../../site/_shortcodes/Aside');

// The first test case checks if the Aside component creates a default aside.
test('creates a default aside', t => {
  // Binding 'this' to the 'thisToBind' object to ensure proper context.
  const thisToBind = {ctx: {locale: 'en'}};

  // Loading the HTML content generated by the Aside component using 'cheerio'.
  const $ = cheerio.load(Aside.bind(thisToBind)('hello world'));

  // Asserting that there is one element with the 'aside' class.
  t.assert($('.aside').length === 1);

  // Asserting that the text content of the 'aside' element is 'hello world'.
  t.assert($('.aside').text().trim() === 'hello world');
});

